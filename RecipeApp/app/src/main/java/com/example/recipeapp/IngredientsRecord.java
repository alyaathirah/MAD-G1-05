package com.example.recipeapp;

public class IngredientsRecord {

    double chicken = 0;
    double flour = 0;
    double garlic = 0;
    double honey = 0;
    double soySos = 0;
    double butter = 0;

    public IngredientsRecord() {
    }

    public double getChicken() {
        return chicken;
    }

    public void setChicken(double chicken) {
        this.chicken = chicken;
    }

    public double getFlour() {
        return flour;
    }

    public void setFlour(double flour) {
        this.flour = flour;
    }

    public double getGarlic() {
        return garlic;
    }

    public void setGarlic(double garlic) {
        this.garlic = garlic;
    }

    public double getHoney() {
        return honey;
    }

    public void setHoney(double honey) {
        this.honey = honey;
    }

    public double getSoySos() {
        return soySos;
    }

    public void setSoySos(double soySos) {
        this.soySos = soySos;
    }

    public double getButter() {
        return butter;
    }

    public void setButter(double butter) {
        this.butter = butter;
    }


    }

